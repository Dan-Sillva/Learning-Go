package main
import "fmt"

// exercicio 2

/*
Escreva expressões utilizando os seguintes operadores, e atribua seus valores a variáveis.

    ==
    !=
    <=
    <

Demonstre estes valores.
*/


func main(){
    z := 10 == 10
    x := 10 != 10
    c := 12 <= 10
    v := 10 < 10

    fmt.Println("z: ", z)
    fmt.Println("x: ", x)
    fmt.Println("c: ", c)
    fmt.Println("v: ", v)

}